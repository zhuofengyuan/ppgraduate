package com.pp.ppgraduate.entity;

public class SortItemModel {
    private int sortItemId;
    private String sortItemName;
    private int sortId;
    private String sortItemPhoto;

    public int getSortItemId() {
        return sortItemId;
    }

    public void setSortItemId(int sortItemId) {
        this.sortItemId = sortItemId;
    }

    public String getSortItemName() {
        return sortItemName;
    }

    public void setSortItemName(String sortItemName) {
        this.sortItemName = sortItemName;
    }

    public int getSortId() {
        return sortId;
    }

    public void setSortId(int sortId) {
        this.sortId = sortId;
    }

    public String getSortItemPhoto() {
        return sortItemPhoto;
    }

    public void setSortItemPhoto(String sortItemPhoto) {
        this.sortItemPhoto = sortItemPhoto;
    }
}
