package com.pp.ppgraduate.entity;

public class ConsigneeModel {
    private int consigneeId;
    private String consigneeAddress;
    private String openId;
    private String consigneeName;
    private String consigneePhone;
    private String consigneeArea;
    private int consigneeDefault;
    private String consigneeDoor;

    public int getConsigneeId() {
        return consigneeId;
    }

    public void setConsigneeId(int consigneeId) {
        this.consigneeId = consigneeId;
    }

    public String getConsigneeAddress() {
        return consigneeAddress;
    }

    public void setConsigneeAddress(String consigneeAddress) {
        this.consigneeAddress = consigneeAddress;
    }

    public String getOpenId() {
        return openId;
    }

    public void setOpenId(String openId) {
        this.openId = openId;
    }

    public String getConsigneeName() {
        return consigneeName;
    }

    public void setConsigneeName(String consigneeName) {
        this.consigneeName = consigneeName;
    }

    public String getConsigneePhone() {
        return consigneePhone;
    }

    public void setConsigneePhone(String consigneePhone) {
        this.consigneePhone = consigneePhone;
    }

    public String getConsigneeArea() {
        return consigneeArea;
    }

    public void setConsigneeArea(String consigneeArea) {
        this.consigneeArea = consigneeArea;
    }

    public int getConsigneeDefault() {
        return consigneeDefault;
    }

    public void setConsigneeDefault(int consigneeDefault) {
        this.consigneeDefault = consigneeDefault;
    }

    public String getConsigneeDoor() {
        return consigneeDoor;
    }

    public void setConsigneeDoor(String consigneeDoor) {
        this.consigneeDoor = consigneeDoor;
    }
}
